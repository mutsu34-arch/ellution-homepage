const KST_OFFSET_MS = 9 * 60 * 60 * 1000;

/** 한국시간(KST) 기준 오늘 날짜 (YYYY-MM-DD) */
export function getKstTodayString(): string {
  return new Date(Date.now() + KST_OFFSET_MS).toISOString().slice(0, 10);
}

/** 공개 예정일(date)이 오늘(KST) 이전이거나 같으면 공개된 글로 간주합니다. */
export function isPublished(post: BlogPost, today: string = getKstTodayString()): boolean {
  return post.date <= today;
}

/** 공개일이 도래한 글만, 최신 날짜순으로 정렬해 반환합니다. (1일 1포스팅 노출용) */
export function getPublishedPosts(): BlogPost[] {
  const today = getKstTodayString();
  return blogPosts
    .filter((post) => post.date <= today)
    .sort((a, b) => (a.date < b.date ? 1 : a.date > b.date ? -1 : 0));
}

export function getPostBySlug(slug: string): BlogPost | undefined {
  return blogPosts.find((post) => post.slug === slug);
}

/** 공개일이 도래한 글만 slug로 조회합니다. (예약 글은 공개 전까지 노출되지 않음) */
export function getPublishedPostBySlug(slug: string): BlogPost | undefined {
  const post = getPostBySlug(slug);
  if (!post) return undefined;
  return isPublished(post) ? post : undefined;
}
