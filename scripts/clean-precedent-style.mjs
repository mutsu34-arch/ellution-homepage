import fs from "fs";

const tsPath = "src/lib/blog-precedent-series.ts";
let html = fs.readFileSync(tsPath, "utf8");
const before = html;

// 1) 장식용 이모지 제거 (의미 있는 화살표 ➔ → ≠ 등은 보존)
const emojis = [
  "🛑", "🛡️", "🛡", "⏰", "👥", "👶", "⚖️", "⚖", "🔵", "🟢", "🔴",
  "⚠️", "⚠", "🎯", "💡", "❌", "📝", "🔥", "🌱", "📚", "📌", "👉",
  "👇", "🚨", "✅", "⭐", "💰", "🚀", "🎓",
];
for (const e of emojis) {
  html = html.split(e + " ").join("");
  html = html.split(e).join("");
}

// 2) 문체 정리 치환 (구체적인 항목 → 일반적인 항목 순서)
const replacements = [
  // 팁 섹션 제목
  [/엘루션 변호사의 수험 밀착 팁(?: \([^)]*\))?/g, "핵심 정리"],
  // 공식 라벨
  [/승리 공식 /g, "정리 "],
  [/승리의 공식/g, "정리"],
  [/패배의 공식/g, "반대 정리"],
  [/핵심 요약 공식/g, "핵심 요약"],
  [/공식 (\d+):/g, "정리 $1:"],
  // 특A급 관련
  [/ ?\(특A급 핵심\)/g, ""],
  [/ ?\(특A급 킬러 지문\)/g, ""],
  [/ ?\(특A급 함정\)/g, " (함정)"],
  [/ ?\(특A급 암기 고속도로\)/g, ""],
  [/\(특A급\): /g, ": "],
  [/ ?\(특A급\)/g, ""],
  [/특A급 쟁점/g, "핵심 쟁점"],
  [/특A급 판례/g, "대표적인 판례"],
  [/특A급/g, ""],
  // 시간 과장 표현
  [/0\.1초 만에 /g, ""],
  [/1초 만에 /g, ""],
  [/암기해 두시면 1초 컷입니다/g, "함께 묶어서 정리해 두면 좋습니다"],
  [/1초 컷/g, "핵심 정리"],
  // 기타 과장/구어 표현
  [/킬러 쟁점/g, "핵심 쟁점"],
  [/머릿속에 도장 찍듯 새겨두세요/g, "정확히 정리해 두세요"],
  [/최빈출 공식들을 모두 쏟아부어 드리겠습니다/g, "최빈출 핵심 내용을 정리하겠습니다"],
  [/"부담 = 독립 만세!"/g, "부담은 독립쟁송 가능"],
  [/전용 무기!/g, "에서만 인정!"],
  // 호칭 정리
  [/수험생 여러분은/g, "수험에서는"],
  [/수험생 여러분/g, "수험생"],
  // 오탈자 교정
  [/기본행위의 무효을 내세워/g, "기본행위의 무효를 내세워"],
  [/과세처분 og 체납처분/g, "과세처분과 체납처분"],
  [/취소소송을 제기겠다/g, "취소소송을 제기하겠다"],
  [/'법이 바뀐 줄 몰랐았다'/g, "'법이 바뀐 줄 몰랐다'"],
];
for (const [pattern, value] of replacements) {
  html = html.replace(pattern, value);
}

// 3) 줄 시작 들여쓰기는 보존하면서, 텍스트 중간의 중복 공백만 정리
html = html.replace(/(\S)  +/g, "$1 ");

if (html === before) {
  console.log("변경 사항이 없습니다.");
} else {
  fs.writeFileSync(tsPath, html, "utf8");
  console.log("문체 정리 완료.");
}
